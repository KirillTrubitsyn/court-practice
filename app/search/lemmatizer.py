"""Лемматизатор русского с in-memory кэшем.

pymorphy3 анализ для каждого токена дорогой. На корпусе в 5к документов
повторяемость лексики ~90%, кэш на словах окупается с лихвой.
"""

from __future__ import annotations

import re
from functools import lru_cache

import pymorphy3


_TOKEN_RE = re.compile(r"[А-Яа-яЁё]+|[A-Za-z]+|\d+", re.UNICODE)

# Минимальные стоп-слова — для русского полноценный список даёт мало пользы при BM25,
# но самые частотные предлоги/союзы стоит выкинуть.
_STOPWORDS: frozenset[str] = frozenset(
    {
        "и", "в", "во", "не", "что", "он", "на", "я", "с", "со", "как", "а", "то",
        "все", "она", "так", "его", "но", "да", "ты", "к", "у", "же", "вы", "за",
        "бы", "по", "только", "ее", "мне", "было", "вот", "от", "меня", "о",
        "из", "ему", "теперь", "когда", "даже", "ну", "вдруг", "ли", "если",
        "уже", "или", "ни", "быть", "был", "него", "до", "вас", "нибудь",
        "опять", "уж", "вам", "ведь", "там", "потом", "себя", "ничего", "ей",
        "может", "они", "тут", "где", "есть", "надо", "ней", "для", "мы", "тебя",
        "их", "чем", "была", "сам", "чтоб", "без", "будто", "чего", "раз", "тоже",
        "себе", "под", "будет", "ж", "тогда", "кто", "этот", "того", "потому",
        "этого", "какой", "совсем", "ним", "здесь", "этом", "один", "почти",
        "мой", "тем", "чтобы", "нее", "сейчас", "были", "куда", "зачем", "всех",
        "никогда", "можно", "при", "наконец", "два", "об", "другой", "хоть",
        "после", "над", "больше", "тот", "через", "эти", "нас", "про", "всего",
        "них", "какая", "много", "разве", "три", "эту", "моя", "впрочем",
        "хорошо", "свою", "этой", "перед", "иногда", "лучше", "чуть", "том",
        "нельзя", "такой", "им", "более", "всегда", "конечно", "всю", "между",
    }
)


_morph = pymorphy3.MorphAnalyzer()


@lru_cache(maxsize=200_000)
def _lemma_cached(token: str) -> str:
    return _morph.parse(token)[0].normal_form


def tokenize(text: str) -> list[str]:
    """Извлечь токены без лемматизации (для отладки/превью)."""
    if not text:
        return []
    return [m.group(0).lower() for m in _TOKEN_RE.finditer(text)]


def lemmatize(text: str) -> list[str]:
    """Токенизация + приведение к нормальной форме + фильтрация стоп-слов и коротышей."""
    if not text:
        return []
    out: list[str] = []
    for match in _TOKEN_RE.finditer(text):
        token = match.group(0).lower()
        if len(token) < 2:
            continue
        if token in _STOPWORDS:
            continue
        # Чисто числовые оставляем как есть — номера дел, статьи, годы.
        if token.isdigit():
            out.append(token)
            continue
        lemma = _lemma_cached(token)
        if lemma in _STOPWORDS:
            continue
        out.append(lemma)
    return out
