class Lemmatizer:
    """Кэширующий лемматизатор. Кэш можно предзагрузить из индекса (ускорение запросов)."""

    def __init__(self, preload_cache: dict[str, str] | None = None) -> None:
        self._morph = pymorphy3.MorphAnalyzer()
        self._cache: dict[str, str] = dict(preload_cache) if preload_cache else {}

    @property
    def cache_size(self) -> int:
        return len(self._cache)

    def cache_snapshot(self) -> dict[str, str]:
        """Копия кэша. Используется build_index для сохранения вместе с индексом."""
        return dict(self._cache)

    def tokenize(self, text: str) -> list[str]:
        if not text:
            return []
        result: list[str] = []
        for token in _TOKEN_RE.findall(text.lower()):
            if len(token) < 2 or token in STOPWORDS:
                continue
            lemma = self._cache.get(token)
            if lemma is None:
                lemma = self._morph.parse(token)[0].normal_form
                self._cache[token] = lemma
            if lemma in STOPWORDS:
                continue
            result.append(lemma)
        return result
