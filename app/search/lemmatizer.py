"""Лемматизатор русского — двухступенчатая фильтрация стоп-слов как в reference/scripts/index.py.

Шаги токенизации:
    1. Извлекаем `[а-яёa-z0-9]+` (нижний регистр).
    2. Отбрасываем токены короче 2 символов и стоп-слова.
    3. Лемматизируем pymorphy3 + кэш.
    4. ПОВТОРНО проверяем стоп-список (формы вида «было → быть» отфильтрует только эта проверка).

Стоп-список совпадает с reference: ~150 общерусских + юр-шум
(`суд, вс, рф, дело, определение, постановление`).
"""

from __future__ import annotations

import re

import pymorphy3


_TOKEN_RE = re.compile(r"[а-яёa-z0-9]+")


# Скопировано один-в-один из reference/scripts/index.py чтобы леммы запросов
# совпадали с леммами корпуса. Любое расхождение — потеря ранкинга.
